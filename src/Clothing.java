public class Clothing {

    String brand;
    String color;
    String price;

    public String getColor() {
        return color;
    }
    public String getBrand() {
        return brand;
    }
    public String getPrice() {
        return price;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setPrice(String price) {
        this.price = price;
    }


}
